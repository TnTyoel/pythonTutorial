#Sorting method
my_tuple = ('dog', 'bird', 'ant', 'cat', 'elephant')
print(sorted(my_tuple))
print(my_tuple)

#Sum method

my_tuple = (5, 10, 15, 20, 25, 30)
print(sum(my_tuple))

#Turning data types into tuple
#string to tuple
print(tuple("Programmer"))

my_string = ("Hello world")
print(tuple(my_string))

#Dictonary to tuple
print(tuple({'Name':'Joshua', 'Animal':'elephant', 'color': 'blue', 'age' : 22} ))

my_dict = {'Name':'Jack', 'Area': 'Florida', 'subscription':'premium'}
print(tuple(my_dict))