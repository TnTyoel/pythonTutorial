#Lists in integer
my_list = [0, 5, 10, 15, 50, 14]
print(my_list)

#lists in string

string_list = ["cat", "tiger", "dog", "lion"]
print(string_list)

#list with integers and strings(mixed data type)
mixed_list = [18, "console", 35.5]
print(mixed_list)

#Nested lists (nested = data stored within data)

nested_list = ["keyboard", 8.5, 6, [5, 1, 3, 4, 5.5, 2]] 
print(nested_list)